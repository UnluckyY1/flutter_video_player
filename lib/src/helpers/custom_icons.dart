import 'package:flutter/material.dart';

/// this class help you to change the default player icons
class CustomIcons {
  final Widget? play,
      pause,
      repeat,
      rewind,
      fastForward,
      sound,
      mute,
      videoFit,
      pip,
      minimize,
      fullscreen;

  const CustomIcons({
    this.play,
    this.pause,
    this.repeat,
    this.rewind,
    this.fastForward,
    this.sound,
    this.mute,
    this.videoFit,
    this.pip,
    this.minimize,
    this.fullscreen,
  });
}
